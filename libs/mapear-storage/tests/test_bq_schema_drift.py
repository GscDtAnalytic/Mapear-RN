# --- Arrow → BQ JSON conversion helpers -------------------------------------


def _pa_type_to_bq(t: pa.DataType) -> str:
    if pa.types.is_string(t):
        return "STRING"
    if pa.types.is_int64(t):
        return "INT64"
    if pa.types.is_float64(t):
        return "FLOAT64"
    if pa.types.is_boolean(t):
        return "BOOL"
    if pa.types.is_timestamp(t):
        return "TIMESTAMP"
    raise AssertionError(f"unmapped pyarrow scalar type: {t}")


def _pa_field_to_bq(field: pa.Field) -> dict:
    t = field.type

    if pa.types.is_list(t):
        value_type = t.value_type
        if pa.types.is_struct(value_type):
            return {
                "name": field.name,
                "type": "RECORD",
                "mode": "REPEATED",
                "fields": [
                    _pa_field_to_bq(value_type.field(i))
                    for i in range(value_type.num_fields)
                ],
            }
        return {
            "name": field.name,
            "type": _pa_type_to_bq(value_type),
            "mode": "REPEATED",
        }

    mode = "NULLABLE" if field.nullable else "REQUIRED"

    if pa.types.is_struct(t):
        return {
            "name": field.name,
            "type": "RECORD",
            "mode": mode,
            "fields": [_pa_field_to_bq(t.field(i)) for i in range(t.num_fields)],
        }

    return {"name": field.name, "type": _pa_type_to_bq(t), "mode": mode}


def _pa_schema_to_bq_json(schema: pa.Schema) -> list[dict]:
    return [_pa_field_to_bq(schema.field(i)) for i in range(len(schema))]


# --- Pydantic introspection helpers -----------------------------------------


def _is_optional(annotation: object) -> bool:
    """True iff annotation is X | None (handles both typing.Union and PEP 604 |)."""
    origin = typing.get_origin(annotation)
    if origin is typing.Union:
        return type(None) in typing.get_args(annotation)
    if isinstance(annotation, types.UnionType):
        return type(None) in typing.get_args(annotation)
    return False


def _pydantic_field_names(model: type[BaseModel]) -> set[str]:
    """All field names: regular model_fields plus @computed_field properties."""
    return set(model.model_fields) | set(model.model_computed_fields)


def _pydantic_annotation(model: type[BaseModel], name: str) -> object:
    if name in model.model_fields:
        return model.model_fields[name].annotation
    cf = model.model_computed_fields.get(name)
    return cf.return_type if cf else None


# --- Tests ------------------------------------------------------------------


@pytest.mark.parametrize("triple", SCHEMA_TRIPLES, ids=lambda t: t.table)
def test_arrow_bq_json_drift(triple: SchemaTriple) -> None:
    """Arrow ↔ BQ JSON: schemas must match exactly, including field order."""
    json_path = triple.bq_json
    assert json_path.exists(), (
        f"BQ schema file missing: {json_path}\n"
        f"  Tabela `{triple.table}` não é Terraform-managed. "
        "Adicionar o JSON e um google_bigquery_table em main.tf.\n"
        "  Ver SCHEMAS.md §4.3."
    )
    on_disk = json.loads(json_path.read_text())
    derived = _pa_schema_to_bq_json(triple.arrow)
    assert derived == on_disk, (
        f"Drift Arrow↔BQ JSON em `{triple.table}`.\n"
        "  Atualizar ambos no mesmo commit para que as tabelas Terraform aceitem "
        "o Parquet carregado pelo pipeline (lição BL-11)."
    )


@pytest.mark.parametrize("triple", SCHEMA_TRIPLES, ids=lambda t: t.table)
def test_pydantic_arrow_field_names(triple: SchemaTriple) -> None:
    """Pydantic ↔ Arrow: field names must match (set equality) + Optional→nullable.

    If a Pydantic field is absent from the Arrow schema, dataframe_to_table drops
    it silently — no error, no warning. This is how D-01 (actor_run_id,
    ingestion_run_id absent from RAW_ARTICLE_SCHEMA) was undetected until B3.

    Nullability rule (unidirectional): Optional[X] in Pydantic → nullable=True in
    Arrow. The converse (nullable Arrow with non-Optional Pydantic) is permissible
    — BQLoader is fail-loud on missing fields, not on null values.

    Limitation — type compatibility is NOT validated here (only field names and
    nullability). A field with Pydantic type `int` mapped to Arrow `pa.string()`
    would pass this test but fail at write time with a pyarrow cast error. Type
    compat is indirectly covered by the Arrow↔BQ JSON ladder: Arrow and BQ JSON
    must agree on type, and BQLoader enforces the BQ type at load time. Direct
    Pydantic→Arrow type checking is tracked as tech debt in
    docs/tech_debt_drift_test_type_coverage.md.
    """
    if triple.pydantic is None:
        pytest.skip(
            f"Sem modelo Pydantic para `{triple.table}` "
            "(G-02: ETL inline sem modelo dedicado)"
        )

    pydantic_names = _pydantic_field_names(triple.pydantic)
    arrow_names = {triple.arrow.field(i).name for i in range(len(triple.arrow))}

    diffs: list[str] = []
    for name in sorted(pydantic_names - arrow_names):
        ann = _pydantic_annotation(triple.pydantic, name)
        diffs.append(
            f"  `{name}` ({ann!r}): presente em Pydantic, AUSENTE em Arrow "
            "→ campo silenciosamente descartado no Parquet"
        )
    for name in sorted(arrow_names - pydantic_names):
        diffs.append(f"  `{name}`: presente em Arrow, AUSENTE em Pydantic")

    if diffs:
        raise AssertionError(
            f"Drift de campo Pydantic↔Arrow em `{triple.table}`:\n"
            + "\n".join(diffs)
            + "\n  Ação: alinhar as duas pontas no mesmo commit."
            + " Ver SCHEMAS.md §4.3 e §6."
        )

    arrow_by_name = {
        triple.arrow.field(i).name: triple.arrow.field(i)
        for i in range(len(triple.arrow))
    }
    null_diffs: list[str] = []
    for name in pydantic_names:
        ann = _pydantic_annotation(triple.pydantic, name)
        if _is_optional(ann) and not arrow_by_name[name].nullable:
            null_diffs.append(
                f"  `{name}` ({ann!r}): Pydantic Optional mas Arrow nullable=False "
                "→ pipeline tentará escrever None num campo REQUIRED"
            )
    if null_diffs:
        raise AssertionError(
            f"Drift de nullabilidade Pydantic↔Arrow em `{triple.table}`:\n"
            + "\n".join(null_diffs)
            + "\n  Ação: marcar o campo Arrow como nullable=True. Ver SCHEMAS.md §4.3."
        )


@pytest.mark.parametrize("triple", SCHEMA_TRIPLES, ids=lambda t: t.table)
def test_pydantic_bq_field_names(triple: SchemaTriple) -> None:
    """Pydantic ↔ BQ JSON: field names must match (set equality) + Optional→NULLABLE.

    mode=NULLABLE is BQ's permissive default; a non-Optional Pydantic field mapped
    to NULLABLE is acceptable (BQLoader is fail-loud on absent fields, not on null).
    """
    if triple.pydantic is None:
        pytest.skip(
            f"Sem modelo Pydantic para `{triple.table}` "
            "(G-02: ETL inline sem modelo dedicado)"
        )

    json_path = triple.bq_json
    if not json_path.exists():
        pytest.fail(
            f"BQ schema file missing: {json_path}\n"
            f"  Não é possível validar Pydantic↔BQ para `{triple.table}` sem o JSON.\n"
            "  Formalizar a tabela no Terraform primeiro (SCHEMAS.md §4.3)."
        )

    bq_schema = json.loads(json_path.read_text())
    bq_by_name = {f["name"]: f for f in bq_schema}
    pydantic_names = _pydantic_field_names(triple.pydantic)
    bq_names = set(bq_by_name)

    diffs: list[str] = []
    for name in sorted(pydantic_names - bq_names):
        ann = _pydantic_annotation(triple.pydantic, name)
        diffs.append(f"  `{name}` ({ann!r}): presente em Pydantic, AUSENTE em BQ JSON")
    for name in sorted(bq_names - pydantic_names):
        diffs.append(f"  `{name}`: presente em BQ JSON, AUSENTE em Pydantic")

    if diffs:
        raise AssertionError(
            f"Drift de campo Pydantic↔BQ JSON em `{triple.table}`:\n"
            + "\n".join(diffs)
            + "\n  Ação: alinhar as duas pontas no mesmo commit. Ver SCHEMAS.md §4.3."
        )

    null_diffs: list[str] = []
    for name in pydantic_names:
        ann = _pydantic_annotation(triple.pydantic, name)
        mode = bq_by_name[name].get("mode", "NULLABLE")
        if _is_optional(ann) and mode == "REQUIRED":
            null_diffs.append(
                f"  `{name}` ({ann!r}): Pydantic Optional mas BQ mode=REQUIRED "
                "→ INSERT com NULL rejeitado pelo BQ"
            )
    if null_diffs:
        raise AssertionError(
            f"Drift de nullabilidade Pydantic↔BQ JSON em `{triple.table}`:\n"
            + "\n".join(null_diffs)
            + "\n  Ação: mudar para mode=NULLABLE. Ver SCHEMAS.md §4.3."
        )
