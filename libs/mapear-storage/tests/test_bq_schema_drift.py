"""Drift test: Pydantic models ↔ pyarrow schemas ↔ BQ JSON schemas.

Each pipeline table has up to three schema representations:
  - Pydantic: the domain model validated at ingest time
  - Arrow: the explicit pa.Schema that writes typed Parquet files
  - BQ JSON: the Terraform-managed schema that pre-provisions the BQ table

All three must stay in sync. Drift between any two causes:
  - Pydantic↔Arrow drift: fields silently dropped when writing Parquet
    (dataframe_to_table projects only Arrow fields — no error, no warning)
  - Arrow↔BQ JSON drift: BQ rejects Parquet on load with
    "Provided Schema does not match Table" (BL-11 incident, 2026-04-19)

Three assertions per triple (skipped when a leg is absent by design):
  1. Arrow ↔ BQ JSON — name-based, order-sensitive.
  2. Pydantic ↔ Arrow — field name set equality + Optional→nullable.
  3. Pydantic ↔ BQ JSON — field name set equality + Optional→NULLABLE.

G-02 gap: silver_social_posts and raw_social_posts_dlq have no dedicated
Pydantic model (transformation is inline, from SocialPost raw). Assertions 2
and 3 are skipped for those triples; Arrow↔BQ JSON (assertion 1) still runs.

Why order matters in assertion 1 even though BQ Parquet loading is name-based:
  - Reordering changes the physical column layout in Parquet, which can break
    tools that read files outside BQ (DuckDB dev, audit scripts, notebooks).
  - Enforcing order guards against auto-formatters silently scrambling the
    intentional grouping conventions (lineage fields last, entity fields grouped).
"""

